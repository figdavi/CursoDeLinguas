    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linha = tabelaAlunos.getSelectedRow();
        if (linha != -1) {
            int matricula = (int) tabelaAlunos.getValueAt(linha, 0);
            int confirm = JOptionPane.showConfirmDialog(this, "Deseja excluir o aluno selecionado?",
                    "Confirmar Exclusão", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                String resultado = alunoController.excluirAluno(matricula);
                JOptionPane.showMessageDialog(this, resultado);
                if (resultado.contains("sucesso")) {
                    atualizarTabela();
                    limparCampos();
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um aluno para excluir.");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        int matricula;
        try {
            matricula = Integer.parseInt(txtMatricula.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Matrícula inválida.");
            return;
        }
        String nome = txtNome.getText().trim();
        String endereco = txtEndereco.getText().trim();
        String telefone = txtTelefone.getText().trim();
        String email = txtEmail.getText().trim();

        String resultado = alunoController.inserirAluno(matricula, nome, endereco, telefone, email);
        JOptionPane.showMessageDialog(this, resultado);
        if (resultado.contains("sucesso")) {
            atualizarTabela();
            limparCampos();
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void tabelaAlunosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaAlunosMouseClicked
        int linha = tabelaAlunos.getSelectedRow();

        if (linha != -1) {
            txtMatricula.setText(tabelaAlunos.getValueAt(linha, 0).toString());
            txtNome.setText(tabelaAlunos.getValueAt(linha, 1).toString());
            txtEndereco.setText(tabelaAlunos.getValueAt(linha, 2).toString());
            txtTelefone.setText(tabelaAlunos.getValueAt(linha, 3).toString());
            txtEmail.setText(tabelaAlunos.getValueAt(linha, 4).toString());

            // Bloqueia edição da matrícula para evitar erro de chave primária
            txtMatricula.setEditable(false);
        }
    }//GEN-LAST:event_tabelaAlunosMouseClicked

