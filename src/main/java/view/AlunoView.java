    private void atualizarTabela() {
        List<model.Aluno> alunos = alunoController.listarTodosAlunos();
        String[] colunas = { "Matrícula", "Nome", "Endereço", "Telefone", "Email" };
        Object[][] dados = new Object[alunos.size()][5];

        for (int i = 0; i < alunos.size(); i++) {
            model.Aluno a = alunos.get(i);
            dados[i][0] = a.getMatricula();
            dados[i][1] = a.getNome();
            dados[i][2] = a.getEndereco();
            dados[i][3] = a.getTelefone();
            dados[i][4] = a.getEmail();
        }

        tabelaAlunos.setModel(new javax.swing.table.DefaultTableModel(dados, colunas) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        });
    }
    
    private void limparCampos() {
        txtMatricula.setText("");
        txtNome.setText("");
        txtEndereco.setText("");
        txtTelefone.setText("");
        txtEmail.setText(""); 
        txtMatricula.setEditable(true);
    }
    
